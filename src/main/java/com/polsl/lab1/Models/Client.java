/**
 * Class representing a client object.
 */
public class Client {
    
    String name;      // Field for storing the client's first name
    String surname;   // Field for storing the client's last name
    Integer age;      // Field for storing the client's age
    Integer phoneNo;  // Field for storing the client's phone number
    Integer id;       // Field for storing the client's ID
    String maill;     // Field for storing the client's email address

    public String getName() {
        return name; // Returns the client's first name
    }

    public void setName(String name) {
        this.name = name; // Sets the client's first name
    }

    public String getSurname() {
        return surname; // Returns the client's last name
    }

    public void setSurname(String surname) {
        this.surname = surname; // Sets the client's last name
    }

    public Integer getAge() {
        return age; // Returns the client's age
    }

    public void setAge(Integer age) {
        this.age = age; // Sets the client's age
    }

    public Integer getPhoneNo() {
        return phoneNo; // Returns the client's phone number
    }

    public void setPhoneNo(Integer phoneNo) {
        this.phoneNo = phoneNo; // Sets the client's phone number
    }

    public Integer getId() {
        return id; // Returns the client's ID
    }

    public void setId(Integer id) {
        this.id = id; // Sets the client's ID
    }

    public String getMaill() {
        return maill; // Returns the client's email address
    }

    public void setMaill(String maill) {
        this.maill = maill; // Sets the client's email address
    }
}

    

    
    
    
    
    
    

